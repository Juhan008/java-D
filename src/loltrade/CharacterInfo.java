package loltrade;

class CharacterInfo {
	public String name="";
	public int ability=0;
	public int cooperation=0;
	public int operate=0; 
	public int hp=0;
	public int position=0;

	public CharacterInfo

	(
			String name,
			int ability,
			int cooperation,
			int operate,
			int hp,
			int position
			
			)
	{
		this.name = name;
		this.ability=ability;
		this.cooperation=cooperation;
		this.operate=operate;
		this.hp=hp;
		this.position=position;
	}
}





	




	
